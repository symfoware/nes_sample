.setcpu		"6502"
.autoimport	on

; iNESヘッダ "HEADER"はcfgのSEGMENTSで定義
.segment "HEADER"
	.byte	$4E, $45, $53, $1A	; "NES" Header
	.byte	$02			; PRG-BANKS
	.byte	$01			; CHR-BANKS
	.byte	$01			; Vetrical Mirror
	.byte	$00			; 
	.byte	$00, $00, $00, $00	; 
	.byte	$00, $00, $00, $00	; 

.segment "STARTUP" ;  "STARTUP"はcfgのSEGMENTSで定義
; リセット割り込み
.proc	Reset
	sei
	ldx	#$ff
	txs

; スクリーンオフ
	lda	#$00
	sta	$2000 ; 基本設定をクリア
	sta	$2001 ; マスク設定をクリア

; パレットテーブルへ転送(BG用のみ転送)
; VRAM BG用パレット $3F00～$3F0F へ転送を実施
	lda	#$3f ; VRAMアドレス上位1byte
	sta	$2006
	lda	#$00 ; VRAMアドレス下位1byte
	sta	$2006 ; $2006 へ2回 store を行うことでVRAMのアクセス先番地を設定
	ldx	#$00
	ldy	#$10
copypal:
	lda	palettes, x ; palettes + xの値をaへロード
	sta	$2007 ; VRAMへデータ書き込み実行
	inx ; xインクリメント
	dey ; yデクリメント
	bne	copypal ; yデクリメントの結果0にならなかったらcopypalに戻る


; ネームテーブルへ転送(画面の中央付近)
; VRAM ネームテーブル $2000～$23BF 画面0のBG配置パターン
	jsr drawchip

; パレット指定
	lda #$23
	sta $2006
	lda #$da
	sta $2006
	lda #%01000000
	sta	$2007
	lda #%11100000
	sta	$2007
	lda #%01000000
	sta	$2007
	lda #%11100000
	sta	$2007

; BGのスクリーン表示位置設定左上にぴったり(スクロール設定)
	lda	#$00
	sta	$2005
	sta	$2005

; スクリーンオン
	lda	#$08 ; 00001000 BGのキャラクタテーブル番号を1に
	sta	$2000
	lda	#$1e ; 00011110 スプライト表示,BG表示,左端8x8のスプライト表示,左端8x8のBG表示
	sta	$2001

; 無限ループ
mainloop:
	jmp	mainloop


; 関数定義テスト
drawchip:
	; map1
	lda	#$20
	sta	$2006
	lda	#$00
	sta	$2006
	
	ldx #$00 ; xを0
	ldy	#$c0 ; ループ回数
copymap1:
	lda	map1, x
	sta	$2007

	inx
	dey
	bne	copymap1

	; map2
	lda	#$20
	sta	$2006
	lda	#$c0
	sta	$2006
	
	ldx #$00 ; xを0
	ldy	#$c0 ; ループ回数
copymap2:
	lda	map2, x
	sta	$2007

	inx
	dey
	bne	copymap2

	; map3
	lda	#$21
	sta	$2006
	lda	#$80
	sta	$2006
	
	ldx #$00 ; xを0
	ldy	#$c0 ; ループ回数
copymap3:
	lda	map3, x
	sta	$2007

	inx
	dey
	bne	copymap3

	; map4
	lda	#$22
	sta	$2006
	lda	#$40
	sta	$2006
	
	ldx #$00 ; xを0
	ldy	#$c0 ; ループ回数
copymap4:
	lda	map4, x
	sta	$2007

	inx
	dey
	bne	copymap4

	; map5
	lda	#$23
	sta	$2006
	lda	#$00
	sta	$2006
	
	ldx #$00 ; xを0
	ldy	#$c0 ; ループ回数
copymap5:
	lda	map5, x
	sta	$2007

	inx
	dey
	bne	copymap5

	rts

.endproc

